// Criando um array com frutas
let frutas = ["banana", "uva", "melancia"];

// Mostra o primeiro elemento
console.log(frutas[0]); // banana

// Mostra quantos elementos existem
console.log(frutas.length); // 3

// Percorre todo o array usando for
for (let i = 0; i < frutas.length; i++) {
  console.log(frutas[i]);
}